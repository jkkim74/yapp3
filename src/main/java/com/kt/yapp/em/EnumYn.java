package com.kt.yapp.em;

/**
 * Y, N or 1, 0 value
 */
public enum EnumYn 
{
	/** Y */
	C_Y("Y")
	/** N */
	, C_N("N")
	/** 1 */
	, C_2("2")
	/** 1 */
	, C_1("1")
	/** 0 */
	, C_0("0")
	/** 17 */
	, C_17("17")
	;
	
	private String value;
	
	private EnumYn(String value) {
		this.value = value;
	}
	
	public String getValue() {
		return this.value;
	}
	
}
